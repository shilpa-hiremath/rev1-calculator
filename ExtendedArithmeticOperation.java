public class ExtendedArithmeticOperation {

    public static float SquareRoot(float op1)
    {
        return op1 * op1;
    }

    public static float CubeRoot(float op1)
    {
        return op1 * op1  * op1;
    }
    public static float modulus(float op1,float op2)
    {
        return op1 % op2;
    }
    
}